//Given an array of integers, count the number of subarrays whose sum is equal to zero.


#include <stdio.h>
#include <stdlib.h>

int main() {
    int n;
    scanf("%d", &n);

    int arr[n];
    for(int i = 0; i < n; i++) {
        scanf("%d", &arr[i]);
    }

    int prefix_sum = 0;
    int count = 0;

    
    int hash[200001] = {0};  
    int offset = 100000;     // To handle negative sums

    hash[offset] = 1;  // prefix sum = 0 initially

    for(int i = 0; i < n; i++) {
        prefix_sum += arr[i];

        if(hash[prefix_sum + offset] > 0) {
            count += hash[prefix_sum + offset];
        }

        hash[prefix_sum + offset]++;
    }

    printf("%d", count);

    return 0;
}
